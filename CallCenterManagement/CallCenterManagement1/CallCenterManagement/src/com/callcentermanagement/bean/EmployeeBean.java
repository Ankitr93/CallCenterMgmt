package com.callcentermanagement.bean;

public class EmployeeBean {
private String empPassword;
private String empName;
private String empDateOfBirth;
private String empSex;
private String emailId;
private String empAddress;
private String empDesignation;
private float empSalary;
private String empDateOfJoin;
private long empMobileNo;
public String getEmpPassword() {
	return empPassword;
}
public void setEmpPassword(String empPassword) {
	this.empPassword = empPassword;
}
public String getEmpName() {
	return empName;
}
public void setEmpName(String empName) {
	this.empName = empName;
}
public String getEmpDateOfBirth() {
	return empDateOfBirth;
}
public void setEmpDateOfBirth(String empDateOfBirth) {
	this.empDateOfBirth = empDateOfBirth;
}
public String getEmpSex() {
	return empSex;
}
public void setEmpSex(String empSex) {
	this.empSex = empSex;
}
public String getEmailId() {
	return emailId;
}
public void setEmailId(String emailId) {
	this.emailId = emailId;
}
public String getEmpAddress() {
	return empAddress;
}
public void setEmpAddress(String empAddress) {
	this.empAddress = empAddress;
}
public String getEmpDesignation() {
	return empDesignation;
}
public void setEmpDesignation(String empDesignation) {
	this.empDesignation = empDesignation;
}
public float getEmpSalary() {
	return empSalary;
}
public void setEmpSalary(float empSalary) {
	this.empSalary = empSalary;
}
public String getEmpDateOfJoin() {
	return empDateOfJoin;
}
public void setEmpDateOfJoin(String empDateOfJoin) {
	this.empDateOfJoin = empDateOfJoin;
}
public long getEmpMobileNo() {
	return empMobileNo;
}
public void setEmpMobileNo(long empMobileNo) {
	this.empMobileNo = empMobileNo;
}

}
