package com.callcentermanagement.bean;

public class CustomerBean {
	private String customerName;
	private String customerSex;
	private String customerDateOfBirth;
	private String customerEmailId;
	private long customerMobileNo;
	private String customerAddress;
	private String customerDateOfRegister;
	public String getCustomerName() {
		return customerName;
	}
	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}
	public String getCustomerSex() {
		return customerSex;
	}
	public void setCustomerSex(String customerSex) {
		this.customerSex = customerSex;
	}
	public String getCustomerDateOfBirth() {
		return customerDateOfBirth;
	}
	public void setCustomerDateOfBirth(String customerDateOfBirth) {
		this.customerDateOfBirth = customerDateOfBirth;
	}
	public String getCustomerEmailId() {
		return customerEmailId;
	}
	public void setCustomerEmailId(String customerEmailId) {
		this.customerEmailId = customerEmailId;
	}
	public long getCustomerMobileNo() {
		return customerMobileNo;
	}
	public void setCustomerMobileNo(long customerMobileNo) {
		this.customerMobileNo = customerMobileNo;
	}
	public String getCustomerAddress() {
		return customerAddress;
	}
	public void setCustomerAddress(String customerAddress) {
		this.customerAddress = customerAddress;
	}
	public String getCustomerDateOfRegister() {
		return customerDateOfRegister;
	}
	public void setCustomerDateOfRegister(String customerDateOfRegister) {
		this.customerDateOfRegister = customerDateOfRegister;
	}
}
